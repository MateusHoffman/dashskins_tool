/**
 * Função para formatar a data no formato DD/MM/YYYY.
 * @param {string} isoDate - Data no formato ISO.
 * @returns {string} - Data formatada.
 */
export const formatDate = (isoDate) => {
  const date = new Date(isoDate);
  const day = String(date.getDate()).padStart(2, '0');
  const month = String(date.getMonth() + 1).padStart(2, '0'); // Meses começam em 0
  const year = date.getFullYear();
  return `${day}/${month}/${year}`;
};

export function transformarString(input) {
  // Remover espaços, pipes e parênteses, e garantir que todos os espaços sejam removidos
  let output = input
    .replace(/\s*\|\s*/g, '-')  // Substituir o pipe (|) por hífens
    .replace(/\s*\(\s*/g, '-')  // Substituir o início de parêntese por hífens
    .replace(/\s*\)\s*/g, '')   // Remover o parêntese de fechamento
    .replace(/\s+/g, '')        // Remover todos os espaços em branco
    .toLowerCase();            // Converter tudo para minúsculas

  return output;
}

