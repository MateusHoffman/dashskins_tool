/**
 * Função para pausar a execução por um tempo especificado com jitter.
 * @param {number} ms - Tempo em milissegundos para pausar.
 * @returns {Promise<void>}
 */
export const delay = (ms) => new Promise((resolve) => setTimeout(resolve, ms));

/**
 * Função para realizar fetch com tentativas de reconexão até sucesso.
 * Implementa backoff exponencial com jitter para melhor tratamento de erros 429.
 * @param {string} url - URL para realizar a requisição.
 * @param {object} options - Opções da requisição (método, headers, agente, etc.).
 * @param {number} initialBackoff - Tempo inicial de espera entre tentativas (em ms).
 * @param {number} maxBackoff - Tempo máximo de espera entre tentativas (em ms).
 * @returns {Promise<Response>} - Resposta da requisição.
 */
export const fetchWithRetries = async (url, options, initialBackoff = 1000, maxBackoff = 60000) => {
  let backoff = initialBackoff;

  while (true) {
    try {
      const response = await fetch(url, options);

      if (response.ok) {
        return response;
      }

      if (response.status === 429) {
        const retryAfter = response.headers.get('Retry-After');
        const waitTime = retryAfter ? parseInt(retryAfter, 10) * 1000 : backoff;
        console.warn(`429 Too Many Requests. Retentando após ${waitTime} ms...`);
        await delay(waitTime + Math.random() * 1000); // Adiciona jitter
      } else if (response.status >= 500 && response.status < 600) {
        // Erros de servidor
        console.warn(`Erro de servidor ${response.status}. Retentando após ${backoff} ms...`);
        await delay(backoff + Math.random() * 1000); // Adiciona jitter
        backoff = Math.min(backoff * 2, maxBackoff); // Exponential backoff
      } else {
        // Outros erros de cliente, não retentar
        console.error(`Erro de cliente ${response.status}. Não será feita nova tentativa.`);
        return response;
      }
    } catch (error) {
      console.warn(`Erro de fetch: ${error.message}. Retentando após ${backoff} ms...`);
      await delay(backoff + Math.random() * 1000); // Adiciona jitter
      backoff = Math.min(backoff * 2, maxBackoff); // Exponential backoff
    }
  }
};

/**
 * Função para escrever dados em um arquivo JSON.
 * @param {string} filePath - Caminho do arquivo.
 * @param {object} data - Dados a serem escritos.
 * @returns {Promise<void>}
 */
export const writeToJsonFile = async (filePath, data) => {
  const fs = await import('fs').then(module => module.promises);
  await fs.writeFile(filePath, JSON.stringify(data, null, 2), 'utf-8');
};

/**
 * Função para ler dados de um arquivo JSON.
 * @param {string} filePath - Caminho do arquivo.
 * @returns {Promise<object>} - Dados lidos do arquivo.
 * @throws {Error} - Lança um erro se a leitura ou o parsing falhar.
 */
export const readFromJsonFile = async (filePath) => {
  try {
    const fs = await import('fs').then(module => module.promises);
    const data = await fs.readFile(filePath, 'utf-8');
    return JSON.parse(data);
  } catch (error) {
    console.error(`Erro ao ler o arquivo JSON em ${filePath}:`, error);
    throw error; // Repassa o erro para ser tratado onde a função for chamada
  }
};

